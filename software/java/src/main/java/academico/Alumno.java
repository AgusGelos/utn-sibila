package academico;

/**
 * Clase que modela un alumno. Gestión básica, solamente a modo de prueba.
 * @author Martin
 */
public class Alumno
{
    /** Nombre del alumno */
    public String Nombre;   
    /** Apellido del alumno */
    public String Apellido;
    /** Número de legajo */
    public String Legajo;
    /** Condición del alumno L = Libre, R = Regular */
    public String Condicion; 
    /** Solamente para gestión del grafo */
    public String IdNodo;
    
}
